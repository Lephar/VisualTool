% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)

global cam;
global lock;
global status;

lock = 0;
status = ~status;

while 1
    if status
        img = snapshot(cam);
        image(img);
        drawnow;
    else
        cla;
        break;
    end
end

function figure1_CloseRequestFcn(hObject, eventdata, handles)

global cam;
global lock;
global status;

while 1
    if status
        status = 0;
    end
    if ~lock
        break;
    end
end

delete(cam);
delete(hObject);
